package mx.edu.utng.tics.arboles_binarios;

/**
 * Clase nodo de un arbol binario
 * @author 1224100719.math@gmail.com
 * @author Manuel Alejandro Tavares Hernandez
 */
public class NodoArbol {
    private int dato;
    
    public NodoArbol hijoIzquierdo;
    public NodoArbol hijoDerecho;

    public NodoArbol(int valor) {
        this.dato = valor;
        this.hijoIzquierdo = hijoIzquierdo;
        this.hijoDerecho = hijoDerecho;
    }

    public int getDato() {
        return dato;
    }

    public void setDato(int nuevoDato) {
        this.dato = nuevoDato;
    }
    
    
    
}
